// Using Message Passing to Transfer Data Between Threads

use std::sync::mpsc;
use std::thread;

fn using_message_passing_to_transfer_data_between_threads_1() {
    let (tx, rx) = mpsc::channel();

    thread::spawn(move || {
        let val = String::from("hi");
        tx.send(val).unwrap();
    });
}

fn using_message_passing_to_transfer_data_between_threads_2() {
    let (tx, rx) = mpsc::channel();

    thread::spawn(move || {
        let val = String::from("hi");
        tx.send(val).unwrap();
    });

    let received = rx.recv().unwrap();
    println!("Got: {}", received);
}

